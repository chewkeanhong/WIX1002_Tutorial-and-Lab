package tutorial_5.T5Q1;

public class T5Q1E {
    public static void main (String [] args){
         int [][] num = {{6,9},{2,5},{4,6}};
         
         for ( int i = 0; i < num.length; i++){
             for (int j = 0; j < num[i].length; j++){
                 System.out.print(num[i][j] + " ");
             }
             System.out.println();
         }
        
   
}
        
    
}
